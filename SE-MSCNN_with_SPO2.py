"""
SE-MSCNN Enhanced with Real SPO2 - Multi-modal Model for Sleep Apnea Detection
===============================================================================

This enhanced version integrates REAL SPO2 saturation data with ECG features.

Data Sources:
  - ECG: UCD Sleep Apnea Database (_lifecard.edf files)
  - SPO2: UCD Sleep Apnea Database (.rec PSG files with oxygen saturation)

Features:
  - ECG: RRI (heart rate variability), Amplitude (R-peak strength)
  - SPO2: Real oxygen saturation levels from pulse oximetry

Architecture:
  - 3 ECG branches (5-min, 3-min, 1-min multi-scale processing)
  - 1 SPO2 branch (8 Hz temporal signal processing)
  - SE attention module for adaptive multi-modal fusion
  
Expected Improvement: +4-6% accuracy with real SPO2 data vs ECG-only baseline
"""

